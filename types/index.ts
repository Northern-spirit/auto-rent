export type BookingStatus = 
  | 'на рассмотрении'
  | 'ожидает оплаты'
  | 'одобрено'
  | 'выполняется'
  | 'ожидает отзыв'
  | 'завершен';

export const getStatusColor = (status: BookingStatus): string => {
  const colors = {
    'на рассмотрении': '#FF9500',
    'ожидает оплаты': '#007AFF',
    'одобрено': '#34C759',
    'выполняется': '#5856D6',
    'ожидает отзыв': '#FF2D55',
    'завершен': '#8E8E93'
  };
  return colors[status] || '#8E8E93'; // Возвращаем серый цвет по умолчанию
}; 